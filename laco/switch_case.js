var dia = "quinta"

switch (dia) {
    case "segunda":
     console.log("dia de começa a semana");
     break;

     case "terça":
     console.log("hora de aprender coisas novas");
     break;

     case "quarta":
     console.log("metade da semana, continue firme");
     break;

     case "quinta":
     console.log("quase la, mais um pouco de esforço");
     break;

     case "sexta":
     console.log("dia de comemorar, o fim de semana chegou");
     break;

     default:
        console.log("final de semana chegou");
}       